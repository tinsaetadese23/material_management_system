package model;

public class User {
private int id;
private String username;
private int rolId;

public int getRolId() {
	return rolId;
}
public void setRolId(int rolId) {
	this.rolId = rolId;
}
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getUsername() {
	return username;
}
public void setUsername(String username) {
	this.username = username;
}


}
