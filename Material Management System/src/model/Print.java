package model;

public class Print {
boolean is_printed;
String issueref;
int subCat;

public boolean isIs_printed() {
	return is_printed;
}
public void setIs_printed(boolean is_printed) {
	this.is_printed = is_printed;
}
public String getIssueref() {
	return issueref;
}
public void setIssueref(String issueref) {
	this.issueref = issueref;
}
public int getSubCat() {
	return subCat;
}
public void setSubCat(int subCat) {
	this.subCat = subCat;
}	
}
