package model;

public class Vehicle {

	int id;
	String chassisNo;
	String plateNo;
	String enginCapacity;
	String cc;
	String noOfWheerls;
	String noOfSeat;
	String model;
	String engineType;
	String color;
	String item;
	
	
	
	
	public String getItem() {
		return item;
	}
	public void setItem(String item) {
		this.item = item;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getChassisNo() {
		return chassisNo;
	}
	public void setChassisNo(String chassisNo) {
		this.chassisNo = chassisNo;
	}
	public String getPlateNo() {
		return plateNo;
	}
	public void setPlateNo(String plateNo) {
		this.plateNo = plateNo;
	}
	public String getEnginCapacity() {
		return enginCapacity;
	}
	public void setEnginCapacity(String enginCapacity) {
		this.enginCapacity = enginCapacity;
	}
	public String getCc() {
		return cc;
	}
	public void setCc(String cc) {
		this.cc = cc;
	}
	public String getNoOfWheerls() {
		return noOfWheerls;
	}
	public void setNoOfWheerls(String noOfWheerls) {
		this.noOfWheerls = noOfWheerls;
	}
	public String getNoOfSeat() {
		return noOfSeat;
	}
	public void setNoOfSeat(String noOfSeat) {
		this.noOfSeat = noOfSeat;
	}
	public String getModel() {
		return model;
	}
	public void setModel(String model) {
		this.model = model;
	}
	public String getEngineType() {
		return engineType;
	}
	public void setEngineType(String engineType) {
		this.engineType = engineType;
	}
	
	
	
}
